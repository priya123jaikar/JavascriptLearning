/////////////////////////////////////loops///////////////////////////////////////////////////

//for loop

for(let i=0 ; i<10 ; i++){
    console.log(i)
}

//while loop
let i =0 
while(i<10){
    console.log(i)
    i++;
}



/////////////////////////////////conditional statement////////////////////////////////////

//if else

var a =3 ;
if(a==3){
    console.log(a)
}
else{
    console.log("wrong")
}


//ternary operator

const b = a>10 ? 'red' : 'blue';
console.log(b)



//switch

var as = "red";

switch(as){
    case 'red':
        console.log("red")
        break;
    
    case 'blue':
        console.log("blue")
        break;
        
    default:
        console.log("not found")
        break;


}





